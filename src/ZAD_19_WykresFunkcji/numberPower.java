package ZAD_19_WykresFunkcji;

public class numberPower {
    private int number;
    private int power;

    public numberPower(int number, int power) {
        this.number = number;
        this.power = power;
    }

    public int getNumber() {
        return number;
    }

    public int getPower() {
        return power;
    }

}
